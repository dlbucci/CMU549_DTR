<p>Below is an example of how a user might interact with the DTR system.</p>

<p>The idea is that the user starts the system and points out where on the camera display the robots are located.</p>

<p>The Central Hub then tells the robots to begin moving, and starts to track them.</p>

<p>As the robots move about the environment, they send messages back to the Central Hub so that it can display what the robots are seeing back to the user.</p>

